# Printing each character of string using loop
name = "Mohammad Arsalan Rather"
for char in name:
    print(char)

# list in python is same as array in C, C++, java
colors = ["Red", "Green", "Black", "White", "Blue"]
for i in colors:
    print(i)

# printing nums from 1-10
for k in range(1, 10):
    print(k)

# printing Odd nums from 1-10
print("\nOddNums")
for k in range(1, 10, 2):
    print(k)

# printing Even nums from 1-10
print("\nEvenNumbs")
for k in range(2, 10, 2):
    print(k)

# printing nums in reverse 10-1
print("\nReverse")
for r in range(10, 0, -1):
    print(r)
